let idade = 20 // tipo number (números)
console.log(typeof 20) // typeof indica o tipo do caractere informado
console.log(typeof idade)

let salario = 337318.58;
console.log(typeof salario);

let estaChovendo = true // ou false (tipo boolean onde os valores são verdadeiro (true) ou falso (false))
console.log(typeof estaChovendo)

let nome = "Caike Machado" // tipo string (informa somente um texto)

console.log(typeof "caike")
console.log(typeof nome)

// Boolean, number e string são os 3 tipos básicos da linguagem javascript (existem outros mais especificos)



