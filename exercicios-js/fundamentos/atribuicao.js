const a = 7
let b = 3

b += a // atribuição aditiva, junto com a atribuição está sendo feita uma adição (b = b + a)
console.log(b)

b -= 4 // b = b - 4
console.log(b)

b *= 2 // b = b * 2
console.log(b)

b /= 2 // b = b / 2
console.log(b)

b %= 2 // b = b % 2 // o módulo "%" é o resto da divisão
console.log(b)


